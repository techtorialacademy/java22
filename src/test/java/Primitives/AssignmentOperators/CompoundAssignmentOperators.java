package Primitives.AssignmentOperators;

public class CompoundAssignmentOperators {

    public static void main(String[] args) {
        int a = 10;
        a += 21; // -> a = a + 21; -> 31
        System.out.println(a); // 31
        a *=3; //  -> a = a * 3; -> 93
        System.out.println(a); // 93
        // These compound assignment operators are used to make
        // code shorter and easier to read.

    }
}
