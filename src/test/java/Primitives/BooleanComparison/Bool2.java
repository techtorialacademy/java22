package Primitives.BooleanComparison;

public class Bool2 {
    public static void main(String[] args) {
        // Print true if the variable `number` is positive and print false if the
        // variable `number` is not positive.
        // Note! 0 is neither a positive nor is negative.
        int number = 0;
        // What makes the number positive?
        // When the number is greater than 0 that makes the number positive.
        boolean isPositive = number > 0;

        System.out.println(isPositive);


    }
}
